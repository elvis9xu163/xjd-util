package com.xjd.util.io.feature;

public class IOFeatureRuntimeException extends RuntimeException {

	public IOFeatureRuntimeException() {
		super();
	}

	public IOFeatureRuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public IOFeatureRuntimeException(String message) {
		super(message);
	}

	public IOFeatureRuntimeException(Throwable cause) {
		super(cause);
	}

}
